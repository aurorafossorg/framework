#include <AuroraFW/Core/DebugManager.h>

namespace AuroraFW {
	bool DebugManager::_status = false;

	void DebugManager::enable(const bool& silent)
	{
		if(_status)
		{
			if(!silent)
				Log("debug is already enabled");
		}
		else
		{
			_status = true;
			if(!silent)
			{
				Log("┌─┐┬ ┬┬─┐┌─┐┬─┐┌─┐  ┌─┐┬─┐┌─┐┌┬┐┌─┐┬ ┬┌─┐┬─┐┬┌─");
				Log("├─┤│ │├┬┘│ │├┬┘├─┤  ├┤ ├┬┘├─┤│││├┤ ││││ │├┬┘├┴┐");
				Log("┴ ┴└─┘┴└─└─┘┴└─┴ ┴  └  ┴└─┴ ┴┴ ┴└─┘└┴┘└─┘┴└─┴ ┴");
				Log("debug is enabled");
			}
		}
	}
	void DebugManager::disable(const bool& silent)
	{
		if(_status == false)
		{
			if(!silent) Log("debug is already disabled");
		}
		else
		{
			_status = false;
			if(!silent) Log("debug is disabled");
		}
	}
}
